// function to convert Celsius
// to Fahrenheit
function celsiusToFahrenheit(celsius) {
    // formula to convert celsius
    // to Fahrenheit
    return (celsius * 9 / 5) + 32;
}

// Driver code
const celsius = 32;
console.log(celsius + " Celsius is " +
    celsiusToFahrenheit(celsius) + " Fahrenheit.");
